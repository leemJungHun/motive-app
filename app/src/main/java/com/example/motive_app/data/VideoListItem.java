package com.example.motive_app.data;

public class VideoListItem {
    private String fileName;
    private String fileUrl;
    private String registerId;
    private String registerName;
    private String registerRelationship;
    private String registrationDate;
    private String registerProfile;
    private String thumbnailUrl;

    public String getFileName() {
        return fileName;
    }

    public void setFileName(String fileName) {
        this.fileName = fileName;
    }

    public String getFileUrl() {
        return fileUrl;
    }

    public void setFileUrl(String fileUrl) {
        this.fileUrl = fileUrl;
    }

    public String getRegisterId() {
        return registerId;
    }

    public void setRegisterId(String registerId) {
        this.registerId = registerId;
    }

    public String getRegisterName() {
        return registerName;
    }

    public void setRegisterName(String registerName) {
        this.registerName = registerName;
    }

    public String getRegisterRelationship() {
        return registerRelationship;
    }

    public void setRegisterRelationship(String registerRelationship) {
        this.registerRelationship = registerRelationship;
    }

    public String getRegistrationDate() {
        return registrationDate;
    }

    public void setRegistrationDate(String registrationDate) {
        this.registrationDate = registrationDate;
    }

    public String getRegisterProfile() {
        return registerProfile;
    }

    public void setRegisterProfile(String registerProfile) {
        this.registerProfile = registerProfile;
    }

    public String getThumbnailUrl() {
        return thumbnailUrl;
    }

    public void setThumbnailUrl(String thumbnailUrl) {
        this.thumbnailUrl = thumbnailUrl;
    }
}
