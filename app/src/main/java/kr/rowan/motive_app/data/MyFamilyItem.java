package kr.rowan.motive_app.data;

public class MyFamilyItem {
    private String myFamilyImgUrl;
    private String myFamilyName;
    private String myFamilyId;
    private String myFamilyGroupCode;
    private String myFamilyRelation;

    public String getMyFamilyImgUrl() {
        return myFamilyImgUrl;
    }

    public void setMyFamilyImgUrl(String myFamilyImgUrl) {
        this.myFamilyImgUrl = myFamilyImgUrl;
    }

    public String getMyFamilyName() {
        return myFamilyName;
    }

    public void setMyFamilyName(String myFamilyName) {
        this.myFamilyName = myFamilyName;
    }

    public String getMyFamilyId() {
        return myFamilyId;
    }

    public void setMyFamilyId(String myFamilyId) {
        this.myFamilyId = myFamilyId;
    }

    public String getMyFamilyRelation() {
        return myFamilyRelation;
    }

    public void setMyFamilyRelation(String myFamilyRelation) {
        this.myFamilyRelation = myFamilyRelation;
    }

    public String getMyFamilyGroupCode() {
        return myFamilyGroupCode;
    }

    public void setMyFamilyGroupCode(String myFamilyGroupCode) {
        this.myFamilyGroupCode = myFamilyGroupCode;
    }
}
